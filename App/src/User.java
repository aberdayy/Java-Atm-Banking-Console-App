public class User {
    int id;
    String name;
    String surname;
    String password;
    int accountNumber;
    double balance;
    public User(int id, String name, String surname, String password, int accountNumber, double balance){
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.password = password;
        this.accountNumber = accountNumber;
        this.balance = balance;
    }
}
