public class User {
    int id;
    String name;
    String surname;
    String password;
    int accountNumber;
    public User(int id, String name, String surname, String password, int accountNumber){
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.password = password;
        this.accountNumber = accountNumber;
    }
}
